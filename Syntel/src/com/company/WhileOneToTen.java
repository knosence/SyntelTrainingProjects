package com.company;

public class WhileOneToTen {

    public static void main(String[] args){

//        1 to 10
//
//        Use a while loop to print the numbers from 1 to 10.
//
//        Example output:
//
//        1
//        2
//        3
//        4
//        5
//        6
//        7
//        8
//        9

        int count = 1;
        while(count < 10){
            System.out.println(count);
            count++;
        }
    }
}
