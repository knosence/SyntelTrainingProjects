package com.company;

import java.util.Scanner;

public class OddNumber {
//    Odd Numbers
//
//    Print each odd number between 1 and 10 inclusive. Use a while loop.
//
//    Example output:
//
//            1
//            3
//            5
//            7
//            9
    public static void main(String[] args){
        int count = 1;
        while(count < 10){
            System.out.println(count);
            count += 2;
        }

    }
}
